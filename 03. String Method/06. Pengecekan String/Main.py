# Pengencekan string akan mengecek boolean dari sebuah string

# isupper() -> True jika semua huruf dalam string adalah huruf besar
print("DICODING".isupper())

# islower() -> True jika semua huruf dalam string adalah huruf kecil
print("dicoding".islower())

# isalpha() -> True jika semua karakter dalam string adalah huruf alfabet
print("dicoding".isalpha())

# isalnum()  -> True jika karakter dalam string adalah alfanumerik
print("dicoding123".isalnum())

# isdecimal() -> True jika karakter dalam string berisi hanya angka/numerik
print("12345".isdecimal())

# isspace() -> True jika string berisi hanya karakter whitespace
print(" ".isspace())

# istitle() -> True jika string berisi huruf kapital di setiap kata
print("Dicoding Indonesia".istitle())