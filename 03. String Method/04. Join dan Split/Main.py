# Menggabungkan dan memisahkan string

# join() -> Menggabungkan sejumlah string
print(" ".join(["Dicoding", "Indonesia", "!"]))

# split() -> Memisahkan substring berdasarkan delimiter tertentu
print("Dicoding Indonesia !".split())
# Memisahkan setiap baris pada string multiline
print('''Halo,
aku ikan,
aku suka sekali menyelam
aku tinggal di perairan.
Badanku licin dan renangku cepat.
Senang berkenalan denganmu.'''.split("\n"))