# Membandingkan kata awalan/akhiran tertentu dan akan menghasilkan nilai boolean

# startswith() -> True jika string diawali dengan kata awalan tertentu yang kita inginkan
print("Dicoding Indonesia".startswith("Dicoding"))

# endswith() -> True jika string diakhiri dengan kata akhiran tertentu yang kita inginkan
print("Dicoding Indonesia".endswith("Indonesia"))