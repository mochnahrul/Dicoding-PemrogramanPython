# Upper dan Lower untuk mengubah huruf besar/kecil

# upper() -> Mengonversi karakter atau string dari huruf kecil ke huruf besar
kata = "dicoding"
kata = kata.upper()
print(kata)

# lower() -> Mengonversi karakter atau string dari huruf besar ke huruf kecil
kata = "DICODING"
kata = kata.lower()
print(kata)