# Input pada string
nama = input("Masukan nama: ")
print(nama)

# Input pada int
angka = int(input("Masukan angka: "))
print(angka)

# Input pada float
angka = float(input("Masukan angka: "))
print(angka)

# Input pada bilangan yang berisi ekspresi matematika
angka = eval(input("Masukan penjumlahan: "))
print(angka)